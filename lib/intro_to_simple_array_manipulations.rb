def using_push(array, string)
  array = ["hi"]
  array.push("Niger")
end 

def using_unshift(array, string)
  array = ["cat", "hi", "wow", "dog", "people"]
  array.unshift("Brooklyn Heights")
end

def using_pop(array)
  array = ["Baby One More Time", "Smells Like Teen Spirit", "Missing", "Walking On The Sun", "Hard Knock Life", "Losing My Religion"]
  array.pop("Losing My Religion")
end

def pop_with_args(array)
  array = ["cat", "hi", "wow", "dog", "people"]
  array.pop("dog", "people")
end
  
def using_shift(array)
  array = ["Lagos", "u", "i"]
  array.shift(2)
end
  
  
  
  
  